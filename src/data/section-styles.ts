// Helper function to generate 26 styles for each section type
const generateSectionStyles = (sectionType: string, modernNames: string[], ultraModernNames: string[]) => {
  const styles = [];

  // Styles 1-11: Modern UI/UX (New styles)
  for (let i = 1; i <= 11; i++) {
    styles.push({
      id: `${i}`,
      name: `${i}. ${modernNames[i - 1] || `Modern Style ${i}`}`
    });
  }

  // Styles 12-26: Ultra-Modern (Existing styles renumbered)
  for (let i = 12; i <= 26; i++) {
    styles.push({
      id: `${i}`,
      name: `${i}. ${ultraModernNames[i - 12] || `Ultra-Modern Style ${i}`}`
    });
  }

  return styles;
};

